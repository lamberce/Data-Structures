package editortrees;

/**
 * Allows for a "mutable" boolean.
 * @author jarvisoa
 *
 */ 
public class BooleanContainer {
	
	private boolean hasBeenRotated;
	
	/**
	 * returns true if it has been rotated.
	 * @return
	 */
	public boolean getHasBeenRotated() {
		return hasBeenRotated;
	}

	/**
	 * sets the hasBeenRotated field to the given bool
	 * 
	 * @param hasBeenRotated
	 */
	public void setHasBeenRotated(boolean hasBeenRotated) {
		this.hasBeenRotated = hasBeenRotated;
	}
	
	/**
	 * Constructs a BooleanContainer with the given value
	 * @param bool
	 */
	public BooleanContainer(boolean bool){
		hasBeenRotated=bool;
	}
}
